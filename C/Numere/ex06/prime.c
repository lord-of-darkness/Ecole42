#include <stdio.h>

int		divizor(int m)
{
	int i;
	int k;

	i = 0;
	for(k = 1; k < m; k++)
	{
		if (m % k == 0)
			i++;
	}
	return (i);
}

int		tru(int n, int m)
{	
	if (divizor(m) == n)
		return (m);
	tru(n, m + 1);
}

int		main()
{
	int l;

	l = 7;
	int m;
	m = 3;
	printf("%d \n",tru(7 , m));
	return (0);
}
