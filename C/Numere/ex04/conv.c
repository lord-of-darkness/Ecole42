#include <stdlib.h>
#include <stdio.h>

int		conv(int n)
{
	while (n != 1)
	{
		if (n % 2 == 0)
		{
			n /= 2;
			printf(" %d ", n);
		}
		else
		{
			n = 3 * n + 1;
			printf(" %d ", n);
		}
	}
	printf("\n");
	return (0);
}

int		main()
{
	int l;

	l = 10;
	conv(l);
	return (0);
}
